package com.project.library;

import com.project.library.db.DB;
import org.springframework.boot.SpringApplication;
import org.springframework.boot.autoconfigure.SpringBootApplication;

import java.sql.Connection;
import java.sql.PreparedStatement;
import java.sql.SQLException;
import java.sql.Statement;


import static com.project.library.db.DB.createTable;
import static com.project.library.db.DB.createTables;


@SpringBootApplication
public class LibraryApplication {

    public static void main(String[] args) throws SQLException {

        DB db = new DB();
        Connection conn = db.dbConnection();
        Statement statement = conn.createStatement();
        PreparedStatement st = conn.prepareStatement("CREATE TABLE hooks " +
                "(id NUMBER GENERATED by default on null as IDENTITY, " +
                "target_url VARCHAR(255) not NULL, " +
                "incoming_url VARCHAR(255) not NULL)");
        DB.createTables(statement);

        SpringApplication.run(LibraryApplication.class,args);
    }
}
